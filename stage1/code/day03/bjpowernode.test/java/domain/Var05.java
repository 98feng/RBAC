package domain;

class Var05
{
	public static void main(String[] args){
		//转义序列符
		//其中\n表示换一行，类似于按一次回车键
		System.out.println("Hello\nWorld");
		//\t表示水平制表符，类似于Tab
		System.out.println("Hello\tWorld");

		//输出HelloWorld
		System.out.println("\"HelloWorld\"");
	
		System.out.println("");
		//\'表示输出一个单引号
		System.out.println("\'HelloWorld\'");
		System.out.println("'HelloWorld'");

		//输出E:\bjpowernode\code\day03\bjpowernode.test\java\domain
		System.out.println("E:\\bjpowernode\\code\\day03\\bjpowernode.test\\java\\domain");
	}
}